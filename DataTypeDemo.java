public class DataTypeDemo 
{
    public static void main(String[] args) 
    {
        int yearOfBirth = 2002;
        float number = 3.200f;
        double piValue = 3.1415926535;
        char currencySymbol = 'a';
        boolean isLoggedIn = true;
        System.out.println("(int): " + yearOfBirth);
        System.out.println("(float): " + number);
        System.out.println("(double): " + piValue);
        System.out.println("(char): " + currencySymbol);
        System.out.println("(boolean): " + isLoggedIn);
    }
}
